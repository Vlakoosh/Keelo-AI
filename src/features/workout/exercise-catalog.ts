const globalMetadata: ExerciseCatalogMetadata = {
  global: true,
};

function createExercise(
  id: string,
  name: string,
  equipment: ExerciseCatalogSeedItem["equipment"],
  muscle: string,
  primaryMuscles: string[],
  secondaryMuscles: string[],
): ExerciseCatalogSeedItem {
  return {
    id,
    name,
    equipment,
    muscle,
    primaryMuscles,
    secondaryMuscles,
    metadata: globalMetadata,
  };
}


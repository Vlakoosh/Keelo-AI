export type ExerciseCatalogMetadata = {
  global: boolean;
};

export type ExerciseCatalogSeedItem = {
  id: string;
  name: string;
  equipment: "Barbell" | "Cable" | "Dumbbell" | "Machine";
  muscle: string;
  primaryMuscles: string[];
  secondaryMuscles: string[];
  metadata: ExerciseCatalogMetadata;
};

